package javaEX;

public class SwitchEx2 {

	public static void main(String[] args) {

		//100-500
		int v=Integer.parseInt(args[0]);
		switch(v) {
		case 100: System.out.println("백이다");
		break;
		case 200: System.out.println("이백이다");
		break;
		case 300: System.out.println("삼백이다");
		break;
		case 400: System.out.println("사백이다");
		break;
		case 500: System.out.println("오백이다");
		break;
		default: System.out.println("없다");
		
		}
		
		
		
		
		
	
	}

}
